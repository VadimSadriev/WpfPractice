namespace WpfPractice.Animation
{
    /// <summary>
    /// Styles of page animations for appearing/disappearing
    /// </summary>
    public enum PageAnimation
    {
        /// <summary>
        /// No animation
        /// </summary>
        None = 0,

        /// <summary>
        /// The pages slides and fades in to the right
        /// </summary>
        SlideAndFadeInFromRight = 1,

        /// <summary>
        /// The pages slides and fades out to the left
        /// </summary>
        SlideAndFadeOutToLeft = 2
    }
}
